/**
 *op_add - fun to add
 *@a: int
 *@b: int
 *Return: res
*/
int op_add(int a, int b)
{
	return (a + b);
}



/**
 *op_sub - fun to sub
 *@a: int
 *@b: int
 *Return: res
*/
int op_sub(int a, int b)
{
	return (a - b);
}



/**
 *op_mul - fun to multiply
 *@a: int
 *@b: int
 *Return: res
*/
int op_mul(int a, int b)
{
	return (a * b);
}



/**
 *op_div - fun to div
 *@a: int
 *@b: int
 *Return: res
*/
int op_div(int a, int b)
{
	return (a / b);
}




/**
 *op_mod - fun to mod
 *@a: int
 *@b: int
 *Return: res
*/
int op_mod(int a, int b)
{
	return (a % b);
}
